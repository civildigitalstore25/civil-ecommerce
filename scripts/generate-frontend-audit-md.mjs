import fs from "node:fs";
import path from "node:path";

const root = path.resolve("frontend");
const outPath = path.resolve("docs/frontend-line-count-and-refactoring-audit.md");
const skip = new Set(["node_modules", "dist", "build", ".git"]);

function walk(dir, out = []) {
  for (const n of fs.readdirSync(dir, { withFileTypes: true })) {
    const p = path.join(dir, n.name);
    if (n.isDirectory()) {
      if (skip.has(n.name)) continue;
      walk(p, out);
    } else {
      const ext = path.extname(n.name).toLowerCase();
      if ([".ts", ".tsx", ".css", ".scss"].includes(ext)) {
        const rel = path.relative(root, p).replace(/\\/g, "/");
        if (rel.includes("node_modules")) continue;
        const lines = fs.readFileSync(p, "utf8").split(/\r?\n/).length;
        out.push({ rel, lines, ext });
      }
    }
  }
  return out;
}

const srcFiles = walk(path.join(root, "src")).sort((a, b) => a.rel.localeCompare(b.rel));
const srcByLines = [...srcFiles].sort((a, b) => b.lines - a.lines);
const over = srcFiles.filter((f) => f.lines > 250);

const buckets = {
  "0-100": 0,
  "101-200": 0,
  "201-250": 0,
  "251-300": 0,
  "301-400": 0,
  "401+": 0,
};
for (const f of srcFiles) {
  if (f.lines <= 100) buckets["0-100"]++;
  else if (f.lines <= 200) buckets["101-200"]++;
  else if (f.lines <= 250) buckets["201-250"]++;
  else if (f.lines <= 300) buckets["251-300"]++;
  else if (f.lines <= 400) buckets["301-400"]++;
  else buckets["401+"]++;
}

